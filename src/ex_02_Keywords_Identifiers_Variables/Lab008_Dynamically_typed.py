"""
Python is Dynamically Typed language.
# At the run time, data type of the variable can be changed. and you don't need to
# mention the data type - Python  - Int is very smart
"""

age = 98
print(type(age)) # 98 (<class 'int'>)
age = "Pramod"
print(type(age)) # Pramode (<class 'str'>)
age = True
print(type(age)) # True (<class 'bool'>)
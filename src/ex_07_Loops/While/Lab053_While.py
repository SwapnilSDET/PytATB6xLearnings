# I - Initialization
# C - Condition
# U - Updation

#----------------------------------
# 10 is included
i = 0 # Initialization
while i <= 10: # Condition
    print(i)
    i = i + 1 #Updation

#----------------------------------
# 10 is not included
i = 0 # Initialization
while i < 10: # Condition
    print(i)
    i = i + 1 #Updation
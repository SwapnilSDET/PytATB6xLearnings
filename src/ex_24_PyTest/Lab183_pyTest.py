# What is pytest
# - `pytest` is a Python testing framework used to write simple to enterprise-grade automated tests.
# - The `pytest` framework makes it easy to write small, readable tests, and can scale to support complex functional testing for applications and libraries.
#
#
# Pytest automatically discovers test files and functions following these conventions:
#
#
# - Test files: test_*.py or *_test.py
# - Test functions: test_*
# - Test classes: Test*
# - Test methods in classes: test_*
